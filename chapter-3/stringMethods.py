str= "shubham sInha"
print("1", str.upper()) # it will convert the string to uppercase
print("2", str.lower()) # it will convert the string to lowercase
print("3", str.capitalize()) # it will capitalize the first character of the string
print("4", str.title()) # it will capitalize the first character of each word in the string
print("5", str.swapcase()) # it will swap the case of each character in the string
print("6", str.count("h")) # it will count the number of occurrences of the character "h" in the string
print("7", str.find("h")) # it will return the index of the first occurrence of the character "h" in the string
print("8", str.replace("h", "H")) # it will replace all occurrences of the character "h" with "H" in the string      
